USE ROLE ACCOUNTADMIN;

CREATE USER IF NOT EXISTS sigma_service_user
  TYPE = SERVICE
  DEFAULT_WAREHOUSE = SIGMA_LIVE_WH;

CREATE ROLE IF NOT EXISTS SIGMA_SERVICE_ROLE;
GRANT ROLE SIGMA_SERVICE_ROLE TO USER sigma_service_user;
ALTER USER sigma_service_user SET DEFAULT_ROLE = SIGMA_SERVICE_ROLE;

GRANT USAGE ON WAREHOUSE SIGMA_LIVE_WH TO ROLE SIGMA_SERVICE_ROLE;
GRANT USAGE ON DATABASE SIGMA_LIVE_MES TO ROLE SIGMA_SERVICE_ROLE;
GRANT USAGE ON SCHEMA SIGMA_LIVE_MES.PUBLIC TO ROLE SIGMA_SERVICE_ROLE;
GRANT SELECT ON ALL TABLES IN SCHEMA SIGMA_LIVE_MES.PUBLIC TO ROLE SIGMA_SERVICE_ROLE;
GRANT SELECT ON ALL VIEWS IN SCHEMA SIGMA_LIVE_MES.PUBLIC TO ROLE SIGMA_SERVICE_ROLE;
GRANT CREATE SCHEMA ON DATABASE SIGMA_LIVE_MES TO ROLE SIGMA_SERVICE_ROLE;

CREATE SCHEMA IF NOT EXISTS SIGMA_LIVE_MES.SIGMA_WRITEBACK;
GRANT ALL ON SCHEMA SIGMA_LIVE_MES.SIGMA_WRITEBACK TO ROLE SIGMA_SERVICE_ROLE;
GRANT CREATE TABLE, CREATE VIEW ON SCHEMA SIGMA_LIVE_MES.SIGMA_WRITEBACK TO ROLE SIGMA_SERVICE_ROLE;


GRANT INSERT ON TABLE SIGMA_LIVE_MES.PUBLIC.STEP_EVENT TO ROLE SIGMA_SERVICE_ROLE;
GRANT USAGE ON PROCEDURE SIGMA_LIVE_MES.PUBLIC.sp_insert_step_event(STRING, STRING, STRING, STRING, STRING) TO ROLE SIGMA_SERVICE_ROLE;